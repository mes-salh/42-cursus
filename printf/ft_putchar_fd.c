#include "ft_printf.h"

void	ft_putchar_fd(char c, int fd, int *j)
{
	int	i;

	if (fd < 0)
		return ;
	i = write (fd, &c, 1);
	if (i == -1)
	{
		*j = -1;
		return ;
	}
	(*j)++;
}
